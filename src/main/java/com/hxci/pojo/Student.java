package com.hxci.pojo;

public class Student {

    private Integer id;
    private String name;
    private Integer CAG;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getCAG() {
        return CAG;
    }

    public void setCAG(Integer CAG) {
        this.CAG = CAG;
    }

}
